#include <stdio.h>

int isBase58(char c);

int isBase58(char c){
  if(('0' < c) && (c <= '9'))   return 1;
  if(c == 'O')                  return 0; 
  if(c == 'I')                  return 0; 
  if(('A' <= c) && (c <= 'Z'))  return 1;
  if(c == 'l')                  return 0; 
  if(('a' <= c) && (c <= 'z'))  return 1;

  return 0;
}


int main(){
  int i;
  int count = 0;
  for(i = 0; i < 255; ++i){
    if(isBase58((char) i)){
      count++;
      printf("%d: %c : %d\n", count, (char) i, i);
    }
  }

  return 0;

}

